//Task 1: Write a function that can calculate the average of an array of values (numbers)

function average(arr) {
    let sum = 0;
    for (let i = 0; i < arr.length; i++) {
        sum += arr[i];
    }

    return sum / arr.length;
}

let arr = [15, 20, 25, 40, 10, 40];
result = average(arr);
console.log(result);

